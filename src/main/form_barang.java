    private void getData() {
        DefaultTableModel model = (DefaultTableModel) tblBarang.getModel();
        model.setRowCount(0);
        
        try{
            Connection connection = Koneksi.getConnection();
            String query = "SELECT * FROM tbl_barang";
            PreparedStatement pst = connection.prepareStatement(query);
            ResultSet rs = pst.executeQuery();
            
            while(rs.next()){
                String kd_brg = rs.getString("kd_brg");
                String kd_jenis = rs.getString("kd_jenis");
                String nama_brg = rs.getString("nama_brg");
                String harga = rs.getString("hrg_brg");
                String satuan = rs.getString("satuan");
                String stok = rs.getString("stok");
                
                
                Object[] rowData = {kd_brg, kd_jenis, nama_brg, harga, satuan, stok};
                model.addRow(rowData);
            }
            
            rs.close();
            connection.close();
            
        } catch (Exception e){
            Logger.getLogger(form_barang.class.getName()).log(Level.SEVERE, null, e);
        }
    }

