    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        // TODO add your handling code here:
        String kd_brg = txtkdbrg.getText();
        String kd_jenis = txtkdjenis.getText();
        String nama_brg = txtnamabrg.getText();
        String satuan = cbsat.getSelectedItem().toString();

        String hrg_brg = txthrgbrg.getText();
        String stok = txtstok.getText();

        
        
        if(kd_brg.isEmpty() || kd_jenis.isEmpty()||nama_brg.isEmpty() || satuan.isEmpty() ||  hrg_brg.isEmpty() || stok.isEmpty() ){
            JOptionPane.showMessageDialog(this, "Semua Kolom Harus Diisi!!", "Validasi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            String sql = "INSERT INTO tbl_barang (kd_brg, kd_jenis, nama_brg, hrg_brg, satuan, stok) VALUES(?,?,?,?,?,?)";
            PreparedStatement st = conn.prepareStatement(sql);
            st.setString(0, kd_brg);
            st.setString(1, kd_jenis);
            st.setString(2, nama_brg);
            st.setString(3, hrg_brg);
            st.setString(4, satuan);
            st.setString(5, stok);

            
            int rowInserted = st.executeUpdate();
            if(rowInserted > 0){
                JOptionPane.showMessageDialog(this, "Data Berhasil Ditambahkan");
                resetForm();
                getData();
            }
            
            st.close();
        } catch (Exception e) {
            Logger.getLogger(form_barang.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_btn_simpanActionPerformed


