    private void getData() {
        DefaultTableModel model = (DefaultTableModel) tbldis.getModel();
        model.setRowCount(0);
        
        try{
            String sql = "Select * From tbl_distributor";
            PreparedStatement st = conn.prepareStatement(sql);
            ResultSet rs = st.executeQuery();
            
            while(rs.next()){
                String id_dis = rs.getString("id_distributor");
                String nama_dis = rs.getString("nama_distributor");
                String telp = rs.getString("telp_distributor");
                String alamat = rs.getString("alamat_distributor");
                
                
                
                Object[] rowData = {id_dis, nama_dis, telp, alamat};
                model.addRow(rowData);
            }
            
            rs.close();
            st.close();
            
        } catch (Exception e){
            Logger.getLogger(listdistri.class.getName()).log(Level.SEVERE, null, e);
        }
    }

    
